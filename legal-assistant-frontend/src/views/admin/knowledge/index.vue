  if (searchQuery.value) {
    const query = searchQuery.value.toLowerCase()
    list = list.filter(doc => 
      doc.filename.toLowerCase().includes(query) ||
      (doc.description && doc.description.toLowerCase().includes(query))
    )
  }
  
  // ÂàÜÁ±ªËøáÊª§
