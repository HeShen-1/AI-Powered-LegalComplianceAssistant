const handleUploadSubmit = async () => {
  if (!uploadFormRef.value) return
  
  try {
    await uploadFormRef.value.validate()
    
    if (fileList.value.length === 0) {
      ElMessage.warning('ËØ∑ÈÄâÊã©Ë¶Å‰∏ä‰º†ÁöÑÊñá‰ª∂')
      return
    }
    
    uploadLoading.value = true
    uploadProgress.value = []
    
    // ÊâãÂä®‰∏ä‰º†ÊØè‰∏™Êñá‰ª∂
    const uploadPromises = fileList.value.map(async (fileItem: any) => {
      const formData = new FormData()
      formData.append('file', fileItem.raw)
      formData.append('category', uploadForm.value.category)
      if (uploadForm.value.description) {
        formData.append('description', uploadForm.value.description)
      }
      
      // Ê∑ªÂä†ËøõÂ∫¶Ë∑üË∏™
      uploadProgress.value.push({
        filename: fileItem.name,
        percentage: 0,
        status: ''
      })
      
      try {
        const response = await fetch(uploadAction.value, {
          method: 'POST',
          headers: {
            'Authorization': uploadHeaders.value.Authorization
          },
          body: formData
        })
        
        const result = await response.json()
        
        // Êõ¥Êñ∞ËøõÂ∫¶
        const progressIndex = uploadProgress.value.findIndex(p => p.filename === fileItem.name)
        if (progressIndex > -1) {
          uploadProgress.value[progressIndex].percentage = 100
          uploadProgress.value[progressIndex].status = result.success ? 'success' : 'exception'
        }
        
        if (result.success) {
          ElMessage.success(`ÊñáÊ°£ ${fileItem.name} ‰∏ä‰º†ÊàêÂäü`)
          return { success: true, filename: fileItem.name }
        } else {
          ElMessage.error(`ÊñáÊ°£ ${fileItem.name} ‰∏ä‰º†Â§±Ë¥•: ${result.message}`)
          return { success: false, filename: fileItem.name }
        }
      } catch (error: any) {
        // Êõ¥Êñ∞ËøõÂ∫¶‰∏∫Â§±Ë¥•
        const progressIndex = uploadProgress.value.findIndex(p => p.filename === fileItem.name)
        if (progressIndex > -1) {
          uploadProgress.value[progressIndex].status = 'exception'
        }
        
        ElMessage.error(`ÊñáÊ°£ ${fileItem.name} ‰∏ä‰º†Â§±Ë¥•`)
        return { success: false, filename: fileItem.name }
      }
    })
    
    // Á≠âÂæÖÊâÄÊúâ‰∏ä‰º†ÂÆåÊàê
    const results = await Promise.all(uploadPromises)
    const successCount = results.filter(r => r.success).length
    const failedCount = results.filter(r => !r.success).length
    
    uploadLoading.value = false
    
    if (failedCount === 0) {
      ElMessage.success(`ÊâÄÊúâÊñáÊ°£‰∏ä‰º†ÊàêÂäüÔºåÂÖ± ${successCount} ‰∏™`)
      // ÂÖ≥Èó≠ÂØπËØùÊ°Ü
      showUploadDialog.value = false
      
      // ÈáçÁΩÆË°®Âçï
      uploadForm.value = {
        category: 'law',
        description: ''
      }
      fileList.value = []
      uploadProgress.value = []
      
      // Âà∑Êñ∞ÂàóË°®
      refreshList()
    } else {
      ElMessage.warning(`‰∏ä‰º†ÂÆåÊàêÔºöÊàêÂäü ${successCount} ‰∏™ÔºåÂ§±Ë¥• ${failedCount} ‰∏™`)
    }
  } catch (error) {
    console.error('Upload failed:', error)
    uploadLoading.value = false
    ElMessage.error('‰∏ä‰º†Â§±Ë¥•')
  }
}

